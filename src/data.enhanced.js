// Function to get recommendations based on query themes
export function getContentRecommendations(queryText) {
  const recommendations = new Set();
  const lowerQuery = queryText.toLowerCase();
  
  Object.entries(CONTENT_THEMES).forEach(([theme, content]) => {
    if (lowerQuery.includes(theme) || theme.split(' ').some(word => lowerQuery.includes(word))) {
      content.forEach(item => recommendations.add(item));
    }
  });
  
  return Array.from(recommendations).slice(0, 5); // Limit to top 5 recommendations
}